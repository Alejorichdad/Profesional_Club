// - ALIANZA -  CARIBE -//

  $(function(){
  $("#activo6").show();
  $(".caribe-titulo").click(function(e){

        e.preventDefault();

        var contenido=$(this).next(".caribe-content");

        if(contenido.css("display")=="none"){ //open

          $(".caribe-titulo").removeClass("open");
          $(".caribe-content").slideUp(550);
          contenido.slideDown(550);
          $(this).addClass("open");
        }
        else{ //close
          contenido.slideUp(550);
          $(this).removeClass("open");
        }
      });
  });

