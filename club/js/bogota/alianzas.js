  $(function(){
  $("#activo24").show();
  $(".adn-titulo").click(function(e){

        e.preventDefault();

        var contenido=$(this).next(".adn-content");

        if(contenido.css("display")=="none"){ //open

          $(".adn-titulo").removeClass("open");
          $(".adn-content").slideUp(550);
          contenido.slideDown(550);
          $(this).addClass("open");
        }
        else{ //close
          contenido.slideUp(550);
          $(this).removeClass("open");
        }
      });
  });

