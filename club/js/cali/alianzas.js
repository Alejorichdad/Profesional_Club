$(function(){
$("#activo70").show();
$(".humantecnology-titulo").click(function(e){

e.preventDefault();

var contenido=$(this).next(".humantecnology-content");

if(contenido.css("display")=="none"){ //open

$(".humantecnology-titulo").removeClass("open");
$(".humantecnology-content").slideUp(550);
contenido.slideDown(550);
$(this).addClass("open");
}
else{ //close
contenido.slideUp(550);
$(this).removeClass("open");
}
});
});



